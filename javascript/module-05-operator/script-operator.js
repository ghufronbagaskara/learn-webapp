let a = 5
let b = "5"

console.log(a == b); // hanya mengecek nilai yang ada

console.log(a === b); // juga mengecek apakah tipe data dan nilai nya sama atau tidak

console.log(a != b);

console.log(a !== b); // mengecek ketidaksamaan nilai dan tipe datanya 

console.log(a < b);

console.log(a > b);

console.log(a <= b);

console.log(a >= b);

